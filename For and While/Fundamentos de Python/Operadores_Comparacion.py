"""
 Operadores de comparacion, como su nombre lo dice nos permite comparar dos valores, dos variables
 o una variable y un valor, como resuldo nos entrega un dato bool, osea verdadero o falso

 mayor que >
 mayor o igual que >=
 menor que <
 menor o igual que <=
 igual a ==
 distinto a !=
 """

variableA = 30
variableB = 50

print( variableA > variableB )
print("La Variable A es Mayor que la B",variableA>variableB)
print("La Variable A es Mayor o Igual que la B",variableA>=variableB)
print("La Variable A es menor que la B",variableA<variableB)
print("La Variable A es menor o Igual que la B",variableA<=variableB)
print("La Variable A es Igual que la B",variableA==variableB)
print("La Variable A es Distinta que la B",variableA!=variableB)
